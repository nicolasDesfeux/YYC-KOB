create TABLE Player (
    id int NOT NULL AUTO_INCREMENT,
    Name varchar(255) NOT NULL,
    hasScore BOOLEAN not null default 0,
    PRIMARY KEY (id)
);

create TABLE Game (
    id int NOT NULL AUTO_INCREMENT,
    sessionDate date NOT NULL,
    highestPoint double,
    lowestPoint DOUBLE,
    isComplete boolean NOT NULL,
